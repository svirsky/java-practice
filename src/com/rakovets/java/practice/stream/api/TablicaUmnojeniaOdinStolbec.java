package com.rakovets.java.practice.stream.api;

public class TablicaUmnojeniaOdinStolbec {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c = 3;
        int d = 4;
        int e = 5;
        int f = 6;
        int g = 7;
        int h = 8;
        int l = 9;
        int k = 10;
        for (int i = 0; i <= 10; i++) {

            System.out.println(i + " x " + a);
            System.out.println(i + " x " + b);
            System.out.println(i + " x " + c);
            System.out.println(i + " x " + d);
            System.out.println(i + " x " + e);
            System.out.println(i + " x " + f);
            System.out.println(i + " x " + g);
            System.out.println(i + " x " + h);
            System.out.println(i + " x " + l);
            System.out.println(i + " x " + k);
        }
    }
}
